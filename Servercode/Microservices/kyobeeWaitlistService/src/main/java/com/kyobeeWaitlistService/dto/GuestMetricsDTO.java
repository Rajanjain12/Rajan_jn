public class GuestMetricsDTO {
	private Integer guestRank;
	private Integer totalWaitTime;
	private Integer guestAheadCount;
	private Integer orgWaitTime;
	
	public Integer getGuestRank() {
		return guestRank;
	}
	public void setGuestRank(Integer guestRank) {
		this.guestRank = guestRank;
	}
	public Integer getTotalWaitTime() {
		return totalWaitTime;
	}
	public void setTotalWaitTime(Integer totalWaitTime) {
		this.totalWaitTime = totalWaitTime;
	}
	public Integer getGuestAheadCount() {
		return guestAheadCount;
	}
	public void setGuestAheadCount(Integer guestAheadCount) {
		this.guestAheadCount = guestAheadCount;
	}
	public Integer getOrgWaitTime() {
		return orgWaitTime;
	}
	public void setOrgWaitTime(Integer orgWaitTime) {
		this.orgWaitTime = orgWaitTime;
	}
	
	
	

	
	

}
